#include<iostream>
using namespace std;

int main(){

    // int
    int count = 5;

    // float
    float share = 3.77;

    // char datatype -- always use ' ' to define
    char alphabet = 'z';

    // Double
    double weight = 55.908765;

    // Boolean
    bool isMale = true;
    bool ischild= 1;
    bool isFemale = 0;

    cout << "count: " << count << endl;
    cout << "share: " << share << endl;
    cout << "alphabet: " << alphabet << endl;
    cout << "weight: " << weight << endl;
    cout << "isMale: " << isMale << endl;
    return 0;
}


